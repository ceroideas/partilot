document.addEventListener('DOMContentLoaded', function() {
    let editMode = false;

    // Toggle edición
    document.getElementById('btn-editar-datos-legales').addEventListener('click', function() {
        editMode = !editMode;
        const inputs = ['nombre-comercial', 'nombre-autonomo', 'nif-cif', 'provincia', 'localidad', 
                       'codigo-postal', 'direccion', 'telefono', 'email'];
        
        inputs.forEach(id => {
            const input = document.getElementById(id);
            input.readOnly = !editMode;
        });

        this.innerHTML = editMode 
            ? '<i class="fe-x me-1"></i> Cancelar'
            : '<i class="fe-edit me-1"></i> Editar';
    });

    // Toggle mostrar contraseña
    document.getElementById('toggle-password').addEventListener('click', function() {
        const passwordInput = document.getElementById('nueva-password');
        const eyeIcon = document.getElementById('eye-icon');
        
        if (passwordInput.type === 'password') {
            passwordInput.type = 'text';
            eyeIcon.classList.remove('fe-eye');
            eyeIcon.classList.add('fe-eye-off');
        } else {
            passwordInput.type = 'password';
            eyeIcon.classList.remove('fe-eye-off');
            eyeIcon.classList.add('fe-eye');
        }
    });

    // Guardar cambios
    document.getElementById('btn-guardar').addEventListener('click', function() {
        // Aquí irá la lógica para guardar los cambios
